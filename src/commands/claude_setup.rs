/// Install global Claude Code hooks to ~/.claude/
pub fn setup_global() -> Result<()> {
    println!("{}", "🛡️  Setting up Claude Code Docker-First hooks...".bright_blue());
    println!();

    // 1. Create hooks directory
    let dir = hooks_dir()?;
    fs::create_dir_all(&dir)
        .with_context(|| format!("Failed to create {}", dir.display()))?;

    // 2. Write docker-first-guard.sh
    let guard_path = dir.join("docker-first-guard.sh");
    fs::write(&guard_path, docker_first_guard_script())
        .with_context(|| format!("Failed to write {}", guard_path.display()))?;
    #[cfg(unix)]
    fs::set_permissions(&guard_path, fs::Permissions::from_mode(0o755))?;
    println!("   {} {}", "✓".green(), guard_path.display().to_string().dimmed());

    // 3. Write stop-test-check.sh
    let stop_path = dir.join("stop-test-check.sh");
    fs::write(&stop_path, stop_test_check_script())
        .with_context(|| format!("Failed to write {}", stop_path.display()))?;
    #[cfg(unix)]
    fs::set_permissions(&stop_path, fs::Permissions::from_mode(0o755))?;
    println!("   {} {}", "✓".green(), stop_path.display().to_string().dimmed());

    // 4. Write global command files (~/.claude/commands/)
    let cmd_dir = commands_dir()?;
    fs::create_dir_all(&cmd_dir)
        .with_context(|| format!("Failed to create {}", cmd_dir.display()))?;

    let pw_path = cmd_dir.join("playwright-cli.md");
    fs::write(&pw_path, playwright_cli_command())
        .with_context(|| format!("Failed to write {}", pw_path.display()))?;
    println!("   {} {}", "✓".green(), pw_path.display().to_string().dimmed());

    // 5. Merge hooks into settings.json
    let settings = settings_path()?;
    let mut value = if settings.exists() {
        let content = fs::read_to_string(&settings)
            .with_context(|| format!("Failed to read {}", settings.display()))?;
        serde_json::from_str(&content)
            .with_context(|| format!("Failed to parse {}", settings.display()))?
    } else {
        serde_json::json!({})
    };

    merge_airis_hooks(&mut value)?;

    let pretty = serde_json::to_string_pretty(&value)?;
    fs::write(&settings, pretty)
        .with_context(|| format!("Failed to write {}", settings.display()))?;
    println!("   {} {}", "✓".green(), settings.display().to_string().dimmed());

    println!();
    println!("{}", "✅ Claude Code hooks installed".green());
    println!();
    println!("  {} Docker-First guard blocks host package managers", "•".dimmed());
    println!("  {} Stop hook runs tests when Claude finishes", "•".dimmed());
    println!("  {} /playwright-cli command for browser automation", "•".dimmed());

    Ok(())
}

/// Show current setup status
pub fn status() -> Result<()> {
    println!("{}", "Claude Code Hook Status".bright_blue().bold());
    println!();

    let dir = hooks_dir()?;

    // Check hook scripts
    let guard_ok = dir.join("docker-first-guard.sh").exists();
    let stop_ok = dir.join("stop-test-check.sh").exists();

    println!("  Hook scripts:");
    print_status("  docker-first-guard.sh", guard_ok);
    print_status("  stop-test-check.sh", stop_ok);

    // Check settings.json
    let settings = settings_path()?;
    let (pre_tool_ok, stop_hook_ok) = if settings.exists() {
        let content = fs::read_to_string(&settings)?;
        let value: Value = serde_json::from_str(&content).unwrap_or(serde_json::json!({}));
        (
            has_airis_hook(&value, "PreToolUse"),
            has_airis_hook(&value, "Stop"),
        )
    } else {
        (false, false)
    };

    // Check command files
    let cmd_dir = commands_dir()?;
    let pw_ok = cmd_dir.join("playwright-cli.md").exists();

    println!();
    println!("  Command files:");
    print_status("  playwright-cli.md", pw_ok);

    println!();
    println!("  settings.json entries:");
    print_status("  PreToolUse (Docker-First guard)", pre_tool_ok);
    print_status("  Stop (test check)", stop_hook_ok);

    let all_ok = guard_ok && stop_ok && pre_tool_ok && stop_hook_ok && pw_ok;
    println!();
    if all_ok {
        println!("{}", "✅ All hooks installed and configured".green());
    } else {
        println!("{}", "⚠️  Some hooks are missing. Run `airis guards install --hooks` to install.".yellow());
    }

    Ok(())
}

/// Remove airis-managed hooks
pub fn uninstall() -> Result<()> {
    println!("{}", "🗑️  Removing airis Claude Code hooks...".bright_blue());
    println!();

    // 1. Remove hooks directory
    let dir = hooks_dir()?;
    if dir.exists() {
        fs::remove_dir_all(&dir)
            .with_context(|| format!("Failed to remove {}", dir.display()))?;
        println!("   {} Removed {}", "✓".green(), dir.display().to_string().dimmed());
    } else {
        println!("   {} {} (not found)", "–".dimmed(), dir.display().to_string().dimmed());
    }

    // 2. Remove airis entries from settings.json
    let settings = settings_path()?;
    if settings.exists() {
        let content = fs::read_to_string(&settings)?;
        let mut value: Value = serde_json::from_str(&content)
            .with_context(|| format!("Failed to parse {}", settings.display()))?;

        remove_airis_hooks(&mut value);

        let pretty = serde_json::to_string_pretty(&value)?;
        fs::write(&settings, pretty)?;
        println!("   {} Cleaned {}", "✓".green(), settings.display().to_string().dimmed());
    }

    println!();
    println!("{}", "✅ airis hooks removed".green());

    Ok(())
}

// ── Internal helpers ────────────────────────────────────────────────

fn print_status(label: &str, ok: bool) {
    if ok {
        println!("  {} {}", "✓".green(), label);
    } else {
        println!("  {} {}", "✗".red(), label);
    }
}

/// Check if settings.json has an airis hook in the given event
fn has_airis_hook(settings: &Value, event: &str) -> bool {
    settings
        .get("hooks")
        .and_then(|h| h.get(event))
        .and_then(|arr| arr.as_array())
        .map(|entries| entries.iter().any(|e| is_airis_entry(e)))
        .unwrap_or(false)
}

/// Check if a hook entry is airis-managed (command contains "airis/")
fn is_airis_entry(entry: &Value) -> bool {
    // Check in hooks array within entry
    if let Some(hooks) = entry.get("hooks").and_then(|h| h.as_array()) {
        return hooks.iter().any(|hook| {
            hook.get("command")
                .and_then(|c| c.as_str())
                .map(|cmd| cmd.contains("airis/"))
                .unwrap_or(false)
        });
    }
    false
}

/// Merge airis hooks into settings.json value, preserving existing user hooks
fn merge_airis_hooks(settings: &mut Value) -> Result<()> {
    // Ensure hooks object exists
    if settings.get("hooks").is_none() {
        settings["hooks"] = serde_json::json!({});
    }

    // Process PreToolUse
    merge_event_hooks(settings, "PreToolUse", airis_pre_tool_use_entry(), true)?;

    // Process Stop
    merge_event_hooks(settings, "Stop", airis_stop_entry(), false)?;

    Ok(())
}

/// Merge a single airis hook entry into an event array
/// If `prepend` is true, adds airis entry at the beginning; otherwise at the end
fn merge_event_hooks(
    settings: &mut Value,
    event: &str,
    airis_entry: Value,
    prepend: bool,
) -> Result<()> {
    let hooks = settings["hooks"]
        .as_object_mut()
        .context("hooks is not an object")?;

    let entries = hooks
        .entry(event)
        .or_insert_with(|| serde_json::json!([]))
        .as_array_mut()
        .context(format!("hooks.{} is not an array", event))?;

    // Remove existing airis entries
    entries.retain(|e| !is_airis_entry(e));

    // Add airis entry
    if prepend {
        entries.insert(0, airis_entry);
    } else {
        entries.push(airis_entry);
    }

    Ok(())
}

/// Remove all airis-managed hooks from settings.json
fn remove_airis_hooks(settings: &mut Value) {
    if let Some(hooks) = settings.get_mut("hooks").and_then(|h| h.as_object_mut()) {
        for (_event, entries) in hooks.iter_mut() {
            if let Some(arr) = entries.as_array_mut() {
                arr.retain(|e| !is_airis_entry(e));
            }
        }
    }
}

// ── Tests ───────────────────────────────────────────────────────────

#[cfg(test)]
mod tests {
    use super::*;
    use serde_json::json;

    #[test]
    fn test_merge_hooks_empty() {
        let mut settings = json!({});
        merge_airis_hooks(&mut settings).unwrap();

        let pre = settings["hooks"]["PreToolUse"].as_array().unwrap();
        assert_eq!(pre.len(), 1);
        assert!(is_airis_entry(&pre[0]));

        let stop = settings["hooks"]["Stop"].as_array().unwrap();
        assert_eq!(stop.len(), 1);
        assert!(is_airis_entry(&stop[0]));
    }

    #[test]
    fn test_merge_hooks_preserves_existing() {
        let user_hook = json!({
            "matcher": "Bash",
            "hooks": [{"type": "command", "command": "my-custom-hook.sh"}]
        });
        let mut settings = json!({
            "hooks": {
                "PreToolUse": [user_hook.clone()],
                "Stop": [user_hook.clone()]
            }
        });

        merge_airis_hooks(&mut settings).unwrap();

        // PreToolUse: airis first, then user
        let pre = settings["hooks"]["PreToolUse"].as_array().unwrap();
        assert_eq!(pre.len(), 2);
        assert!(is_airis_entry(&pre[0])); // airis prepended
        assert!(!is_airis_entry(&pre[1])); // user preserved

        // Stop: user first, then airis
        let stop = settings["hooks"]["Stop"].as_array().unwrap();
        assert_eq!(stop.len(), 2);
        assert!(!is_airis_entry(&stop[0])); // user preserved
        assert!(is_airis_entry(&stop[1])); // airis appended
    }

    #[test]
    fn test_merge_hooks_replaces_airis() {
        let mut settings = json!({});
        merge_airis_hooks(&mut settings).unwrap();
        // Run again — should not duplicate
        merge_airis_hooks(&mut settings).unwrap();

        let pre = settings["hooks"]["PreToolUse"].as_array().unwrap();
        assert_eq!(pre.len(), 1);

        let stop = settings["hooks"]["Stop"].as_array().unwrap();
        assert_eq!(stop.len(), 1);
    }

    #[test]
    fn test_generate_guard_script() {
        let script = docker_first_guard_script();
        assert!(script.contains("BLOCKED=("));
        assert!(script.contains("npm (install|i |ci|add|update|remove|uninstall)"));
        assert!(script.contains("pip3? install"));
        assert!(script.contains("brew install"));
        assert!(script.contains("exit 2"));
    }

    #[test]
    fn test_uninstall_removes_airis_only() {
        let user_hook = json!({
            "matcher": "Bash",
            "hooks": [{"type": "command", "command": "my-custom-hook.sh"}]
        });
        let mut settings = json!({});
        merge_airis_hooks(&mut settings).unwrap();

        // Add a user hook
        settings["hooks"]["PreToolUse"]
            .as_array_mut()
            .unwrap()
            .push(user_hook.clone());

        // Uninstall
        remove_airis_hooks(&mut settings);

        let pre = settings["hooks"]["PreToolUse"].as_array().unwrap();
        assert_eq!(pre.len(), 1);
        assert!(!is_airis_entry(&pre[0])); // only user hook remains
    }
}
